export function getCareerDirectoryMatchesForSchool(query: string) {
  const normalizedQuery = normalizeCareerDirectoryText(query);
  if (!normalizedQuery) return [];
  const exactMatches = schoolCareerDirectoryEntries.filter((entry) => normalizeCareerDirectoryText(entry.schoolName) === normalizedQuery);
  if (exactMatches.length) return exactMatches;
  return schoolCareerDirectoryEntries.filter((entry) => {
    const normalizedSchool = normalizeCareerDirectoryText(entry.schoolName);
    return normalizedSchool.includes(normalizedQuery) || normalizedQuery.includes(normalizedSchool);
  });
}

function normalizeCareerDirectoryText(value: string) {
  return value.toLowerCase().replace(/[\s??()??,??.-]+/g, "").trim();
}
